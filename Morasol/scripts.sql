CREATE TABLE TRESE
(
  TRESE_HOTEL     VARCHAR2(15 BYTE),
  TRESE_CODI      NUMBER,
  TRESE_ANCI      NUMBER,
  TRESE_DESGLOSE  NUMBER,
  TRESE_HOTE      VARCHAR2(30 BYTE),
  TRESE_DACR      DATE,
  TRESE_CLIE      VARCHAR2(50 BYTE),
  TRESE_TTOO      VARCHAR2(50 BYTE),
  TRESE_AGENCIA      VARCHAR2(50 BYTE),
  TRESE_DAEN      DATE,
  TRESE_DASA      DATE,
  TNACI_CODI      VARCHAR2(3 BYTE),
  TRESE_THABRESE  VARCHAR2(10 BYTE),
  TRESE_THABOCUP  VARCHAR2(10 BYTE),
  TRESE_TREG      VARCHAR2(5 BYTE)
)
TABLESPACE USERS
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING;


CREATE UNIQUE INDEX TRESE_PK ON TRESE
(TRESE_HOTEL, TRESE_CODI, TRESE_ANCI, TRESE_DESGLOSE)
LOGGING
TABLESPACE USERS
PCTFREE    10
INITRANS   2
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
NOPARALLEL;


ALTER TABLE TRESE ADD (
  CONSTRAINT TRESE_PK
  PRIMARY KEY
  (TRESE_HOTEL, TRESE_CODI, TRESE_ANCI, TRESE_DESGLOSE)
  USING INDEX TRESE_PK);




