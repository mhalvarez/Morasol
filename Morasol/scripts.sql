

CREATE USER MORASOL
  IDENTIFIED BY MORASOL
  DEFAULT TABLESPACE USERS
  TEMPORARY TABLESPACE TEMP
  PROFILE DEFAULT
  ACCOUNT UNLOCK;
  -- 3 Roles for MORASOL 
  GRANT EXP_FULL_DATABASE TO MORASOL;
  GRANT IMP_FULL_DATABASE TO MORASOL;
  GRANT CONNECT TO MORASOL;
  ALTER USER MORASOL DEFAULT ROLE ALL;
  -- 4 System Privileges for MORASOL 
  GRANT UNLIMITED TABLESPACE TO MORASOL;
  GRANT DEBUG CONNECT SESSION TO MORASOL;
  GRANT SELECT ANY TABLE TO MORASOL;
  GRANT DEBUG ANY PROCEDURE TO MORASOL;
  


